package net.xprova.xprova;

public class FlipFlop {

	public String moduleName, clkPort, rstPort, dPort;

	public FlipFlop(String moduleName, String clkPort, String rstPort, String dPort) {

		this.moduleName = moduleName;
		this.clkPort = clkPort;
		this.rstPort = rstPort;
		this.dPort = dPort;

	}

}
